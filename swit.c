#include <stdio.h>

//switch statment

int main(void){
    const int day = 7;

    switch (day){
        case 1:
        printf("Sunday\n");
        break;
    
        case 2:
        printf("Monday\n");
        break;

        case 3:
        printf("Tuesday\n");
        break;

        case 4:
        printf("Wednesday\n");
        break;

        case 5:
        printf("Thursday\n");
        break;

        case 6:
        printf("Friday\n");
        break;
        default:

        case 7:
        printf("Saturday\n");

}
    }